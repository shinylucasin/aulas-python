# Estrutura condicional "if", que executa um bloco de código se uma condição for verdadeira.

idade = 18

if idade < 18: # Verifica se a idade é menor que 18
    print("Você é menor de idade.")
elif idade == 18: # Verifica se a idade é igual a 18
    print("Você tem exatamente 18 anos.")
else: # Se a idade não for menor nem igual a 18, então é maior
    print("Você é maior de idade.")